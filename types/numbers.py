import random

#  Three basic types of numbers depending upon their values

integer_type = 242424
float_type = 3.17
complex_type = 676j

print(type(integer_type))  # <class 'int'>
print(type(float_type))  # <class 'float'>
print(type(complex_type))  # <class 'complex'>

print(random.randrange(1, 10))  # Prints a random integer
